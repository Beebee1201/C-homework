#include <stdio.h>

int main() {
    for(int i = 1;i<=9;i++){
        printf("%d*1=%d\t",i,i*1);
        printf("%d*2=%d\t",i,i*2);
        printf("%d*3=%d\t",i,i*3);
        printf("%d*4=%d\t",i,i*4);
        printf("%d*5=%d\t",i,i*5);
        printf("%d*6=%d\t",i,i*6);
        printf("%d*7=%d\t",i,i*7);
        printf("%d*8=%d\t",i,i*8);
        printf("%d*9=%d\n",i,i*9);
        
    }

    return 0;
}
